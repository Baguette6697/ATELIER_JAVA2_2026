public class Voiture {
    // Attributes (Fields)
    private String marque;
    private String modele; // Assuming "voiture" meant the model of the car
    private int annee;

    // Constructor (Used to create a new Voiture object)
    public Voiture(String marque, String modele, int annee) {
        this.marque = marque;
        this.modele = modele;
        this.annee = annee;
    }

    // Getters and Setters (To access and modify the attributes)
    public String getMarque() {
        return marque;
    }

    public void setMarque(String marque) {
        this.marque = marque;
    }

    public String getModele() {
        return modele;
    }

    public void setModele(String modele) {
        this.modele = modele;
    }

    public int getAnnee() {
        return annee;
    }

    public void setAnnee(int annee) {
        this.annee = annee;
    }
}
